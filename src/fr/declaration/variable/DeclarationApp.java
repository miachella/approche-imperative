package fr.declaration.variable;

public class DeclarationApp {
	public static void main(String[] args) {
		byte monPremierNumero = -3;
		short leNumeroEstPlusLong = 48;
		int tati = 100008;
		long plop = 2020L;
		float pi = 3.14F;
		double benoit = 16.0;
		char papa = 'f';
		boolean vraiOuFaux = true;
		String myString = "Coucou, comment va?";
		
		System.out.println(vraiOuFaux);
		System.out.println("c'est vraiment trop cool Java!");
		
		String randomString = "Voici le résultat d’un calcul : 1+5=6";
		randomString = "Voici le résultat d’un calcul : \n1+5=6";
		
		System.out.println(randomString);
	}

}
